// criação de uma função
fun main() {
    // o Kotlin entende por si só o tipo de variável declarada
    
    // variável tipo string
    var primeiroNome = "Ryu"
    
    // variável tipo Int
    var idade = 85
    
    // variável Boolean. Se for do gosto do programador, 
    // é possível declarar o tipo da variável também. 
    var myVar1: Boolean = true
    
    // variável tipo Double
    var peso: Double = 85.5
    
    // constante que armazena o valor de pi. Uma val é imutável.package
    val pi = 3.141592
}